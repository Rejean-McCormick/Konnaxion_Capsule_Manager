def render_result_panel(result: Any | None) -> str:
    """Render a normalized GUI action result."""

    if result is None:
        return ""

    data = to_mapping(result)

    if data:
        ok = bool(data.get("ok", data.get("success", False)))
        action = data.get("action", data.get("operation", ""))
        message = data.get("message", "")
        instance_id = data.get("instance_id", "")
        payload = data.get("data", data.get("payload", None))
    else:
        ok = bool(getattr(result, "ok", getattr(result, "success", False)))
        action = getattr(result, "action", getattr(result, "operation", ""))
        message = getattr(result, "message", "")
        instance_id = getattr(result, "instance_id", "")
        payload = getattr(result, "data", getattr(result, "payload", None))

    tone = "ok" if ok else "error"

    body = f"<p>{h(message or ('Action completed.' if ok else 'Action failed.'))}</p>"
    body += render_definition_list(
        {
            "ok": ok,
            "action": action,
            "instance_id": instance_id,
        }
    )

    if payload not in (None, {}, []):
        body += "<h3>Data</h3>" + render_json_block(payload)

    return render_card("Result", body, classes=f"kx-result {tone}")


