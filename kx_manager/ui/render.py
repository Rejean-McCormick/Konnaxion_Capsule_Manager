def render_value(value: Any) -> str:
    """Render scalar/sequence/mapping values safely."""

    if value is None:
        return '<span class="kx-muted">—</span>'

    if isinstance(value, SafeHtml):
        return str(value)

    if isinstance(value, bool):
        return render_status("true" if value else "false")

    if isinstance(value, Enum):
        return h(value.value)

    if isinstance(value, Mapping):
        return render_json_block(value)

    to_dict = getattr(value, "to_dict", None)
    if callable(to_dict):
        return render_json_block(to_dict())

    model_dump = getattr(value, "model_dump", None)
    if callable(model_dump):
        return render_json_block(model_dump())

    if isinstance(value, (list, tuple, set)) and not isinstance(value, (str, bytes)):
        if not value:
            return '<span class="kx-muted">—</span>'
        return "<ul>" + "".join(f"<li>{render_value(item)}</li>" for item in value) + "</ul>"

    return h(value)


def render_definition_list(items: Mapping[str, Any] | Sequence[tuple[str, Any]]) -> str:
    """Render key/value pairs as a definition list."""

    pairs = items.items() if isinstance(items, Mapping) else items
    rows = [f"<dt>{h(key)}</dt><dd>{render_value(value)}</dd>" for key, value in pairs]
    return safe_html(f'<dl class="kx-defs">{"".join(rows)}</dl>')


def render_table(rows: Sequence[Mapping[str, Any]], columns: Sequence[str] | None = None) -> str:
    """Render a safe table from mapping rows."""

    if not rows:
        return '<p class="kx-muted">No records found.</p>'

    if columns is None:
        columns = list(rows[0].keys())

    head = "".join(f"<th>{h(column)}</th>" for column in columns)
    body_rows: list[str] = []

    for row in rows:
        cells = "".join(f"<td>{render_value(row.get(column))}</td>" for column in columns)
        body_rows.append(f"<tr>{cells}</tr>")

    return safe_html(
        '<table class="kx-table">'
        f"<thead><tr>{head}</tr></thead>"
        f"<tbody>{''.join(body_rows)}</tbody>"
        "</table>"
    )


