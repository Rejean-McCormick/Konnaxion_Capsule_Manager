def render_badge(label: Any, tone: str = "info") -> str:
    """Render a small status badge."""

    normalized = tone if tone in {"ok", "warn", "error", "info"} else "info"
    return safe_html(f'<span class="kx-badge {normalized}">{h(label)}</span>')


def status_tone(value: Any) -> str:
    """Map canonical status-ish values to badge tones."""

    text = str(raw_value(value) or "").lower()

    if text in {
        "ok",
        "pass",
        "passed",
        "ready",
        "running",
        "connected",
        "verified",
        "restored",
        "completed",
        "healthy",
        "true",
        "success",
    }:
        return "ok"

    if text in {
        "warn",
        "warning",
        "degraded",
        "skipped",
        "unknown",
        "planned",
        "starting",
        "stopping",
        "updating",
        "rolling_back",
        "preflight",
        "verifying",
    }:
        return "warn"

    if text in {
        "fail",
        "failed",
        "fail_blocking",
        "error",
        "security_blocked",
        "blocked",
        "quarantined",
        "disconnected",
        "unauthorized",
        "unhealthy",
        "false",
    }:
        return "error"

    return "info"


