def raw_value(value: Any) -> Any:
    """Return a scalar value for enums and enum-like values."""

    return getattr(value, "value", value)


def h(value: Any) -> str:
    """Escape text for safe HTML rendering."""

    if value is None:
        return ""
    return escape(str(raw_value(value)), quote=True)


def attr(value: Any) -> str:
    """Escape an HTML attribute value."""

    return h(value)


def css_class(*parts: str | None | bool) -> str:
    """Join CSS class fragments while ignoring blanks and false values."""

    return " ".join(str(part) for part in parts if part)


def to_mapping(value: Any) -> Mapping[str, Any]:
    """Convert DTO/view models to dictionaries when possible."""

    if isinstance(value, Mapping):
        return value

    if is_dataclass(value):
        return asdict(value)

    to_dict = getattr(value, "to_dict", None)
    if callable(to_dict):
        data = to_dict()
        if isinstance(data, Mapping):
            return data

    model_dump = getattr(value, "model_dump", None)
    if callable(model_dump):
        data = model_dump()
        if isinstance(data, Mapping):
            return data

    dict_method = getattr(value, "dict", None)
    if callable(dict_method):
        data = dict_method()
        if isinstance(data, Mapping):
            return data

    return {}


def json_text(value: Any) -> str:
    """Serialize arbitrary data for display."""

    def default(item: Any) -> Any:
        if isinstance(item, Enum):
            return item.value

        if is_dataclass(item):
            return asdict(item)

        to_dict = getattr(item, "to_dict", None)
        if callable(to_dict):
            return to_dict()

        model_dump = getattr(item, "model_dump", None)
        if callable(model_dump):
            return model_dump()

        return str(item)

    try:
        return json.dumps(value, indent=2, sort_keys=True, default=default)
    except TypeError:
        return str(value)


def safe_href(href: str | None) -> str:
    """Return an escaped safe link target."""

    raw = (href or "#").strip()
    lowered = raw.lower()

    if not raw or lowered.startswith(("javascript:", "data:", "vbscript:")):
        return "#"

    return attr(raw)


def action_url(action: Any) -> str:
    """Build the canonical UI action route for a UiAction/string value."""

    value = raw_value(action)

    try:
        return _route_for_action(value)
    except Exception:
        text = str(value).strip()
        if not text:
            return "#"
        return f"{ACTION_BASE_PATH}/{text.replace('_', '-')}"


def _form_action_href(action: str) -> str:
    raw = str(action).strip()
    lowered = raw.lower()

    if (
        raw.startswith("/")
        or raw.startswith("#")
        or lowered.startswith(("http://", "https://"))
    ):
        return raw

    return action_url(raw)


def default_nav(active_href: str = UI_BASE_PATH) -> list[NavItem]:
    """Return default local GUI navigation."""

    return [
        NavItem(label=label, href=href, active=href == active_href)
        for label, href in NAV_ITEMS
    ]


def render_nav(items: Sequence[NavItem | Mapping[str, Any]]) -> str:
    """Render the top navigation bar."""

    links: list[str] = []

    for item in items:
        if isinstance(item, Mapping):
            label = item.get("label", "")
            href = item.get("href", "#")
            active = bool(item.get("active"))
        else:
            label = item.label
            href = item.href
            active = item.active

        links.append(
            f'<a class="{css_class("active" if active else None)}" '
            f'href="{safe_href(str(href))}">{h(label)}</a>'
        )

    return safe_html(f'<nav class="kx-nav">{"".join(links)}</nav>')


