def render_select(
    name: str,
    label: str,
    options: Sequence[SelectOption | tuple[str, str] | str],
    *,
    value: Any = "",
    required: bool = False,
    help_text: str = "",
) -> str:
    current = str(raw_value(value) or "")
    option_html: list[str] = []

    for option in options:
        if isinstance(option, SelectOption):
            option_value = option.value
            option_label = option.label
            selected = option.selected or option.value == current
        elif isinstance(option, tuple):
            option_value, option_label = option
            selected = str(option_value) == current
        else:
            option_value = str(raw_value(option))
            option_label = option_value
            selected = option_value == current

        selected_attr = " selected" if selected else ""
        option_html.append(
            f'<option value="{attr(option_value)}"{selected_attr}>{h(option_label)}</option>'
        )

    required_attr = " required" if required else ""
    help_html = f'<div class="kx-help">{h(help_text)}</div>' if help_text else ""

    return safe_html(
        '<div class="kx-field">'
        f'<label for="{attr(name)}">{h(label)}</label>'
        f'<select id="{attr(name)}" name="{attr(name)}"{required_attr}>{"".join(option_html)}</select>'
        f"{help_html}"
        "</div>"
    )


def render_checkbox(
    name: str,
    label: str,
    *,
    checked: bool = False,
    value: str = "true",
    help_text: str = "",
) -> str:
    checked_attr = " checked" if checked else ""
    help_html = f'<div class="kx-help">{h(help_text)}</div>' if help_text else ""

    return safe_html(
        '<div class="kx-field">'
        f'<label><input name="{attr(name)}" type="checkbox" value="{attr(value)}"{checked_attr}> {h(label)}</label>'
        f"{help_html}"
        "</div>"
    )


def render_fields(fields: Sequence[FormField | Mapping[str, Any]]) -> str:
    """Render declarative form fields."""

    rendered: list[str] = []

    for field in fields:
        item = FormField(**field) if isinstance(field, Mapping) else field

        if item.field_type == "hidden":
            rendered.append(render_hidden(item.name, item.value))
        elif item.field_type == "textarea":
            rendered.append(
                render_textarea(
                    item.name,
                    item.label,
                    item.value,
                    required=item.required,
                    placeholder=item.placeholder,
                    help_text=item.help_text,
                )
            )
        elif item.field_type == "select":
            rendered.append(
                render_select(
                    item.name,
                    item.label,
                    item.options or [],
                    value=item.value,
                    required=item.required,
                    help_text=item.help_text,
                )
            )
        elif item.field_type == "checkbox":
            rendered.append(
                render_checkbox(
                    item.name,
                    item.label,
                    checked=bool(item.value),
                    help_text=item.help_text,
                )
            )
        else:
            rendered.append(
                render_input(
                    item.name,
                    item.label,
                    item.value,
                    field_type=item.field_type,
                    required=item.required,
                    placeholder=item.placeholder,
                    help_text=item.help_text,
                )
            )

    return safe_html("".join(rendered))


def render_form(
    action: str,
    fields: Sequence[FormField | Mapping[str, Any]] | str = "",
    *,
    method: str = "post",
    submit_label: str = "Submit",
    hidden: Mapping[str, Any] | None = None,
    extra_actions: str = "",
    classes: str = "",
) -> str:
    """Render a safe HTML form."""

    method_value = "post" if method.lower() == "post" else "get"
    hidden_html = "".join(render_hidden(key, value) for key, value in (hidden or {}).items())
    fields_html = fields if isinstance(fields, str) else render_fields(fields)
    form_action = _form_action_href(action)

    return safe_html(
        f'<form class="{css_class("kx-form", classes)}" method="{attr(method_value)}" action="{safe_href(form_action)}">'
        f"{hidden_html}"
        f"{fields_html}"
        '<div class="kx-actions">'
        f'<button class="kx-button" type="submit">{h(submit_label)}</button>'
        f"{extra_actions}"
        "</div>"
        "</form>"
    )


def render_action_form(
    action: Any,
    label: str,
    *,
    payload: Mapping[str, Any] | None = None,
    variant: str = "primary",
    disabled: bool = False,
) -> str:
    """Render a one-button POST form for a canonical GUI action."""

    action_value = raw_value(action)
    disabled_attr = " disabled" if disabled else ""
    button_class = css_class(
        "kx-button",
        "secondary" if variant == "secondary" else None,
        "danger" if variant == "danger" else None,
    )
    hidden_payload = {"action": action_value, **dict(payload or {})}
    hidden_html = "".join(render_hidden(key, value) for key, value in hidden_payload.items())

    return safe_html(
        f'<form method="post" action="{safe_href(action_url(action_value))}" style="display:inline">'
        f"{hidden_html}"
        f'<button class="{button_class}" type="submit"{disabled_attr}>{h(label)}</button>'
        "</form>"
    )


